import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import matter from "gray-matter";
import { codeToHtml } from "shiki";
import { z } from "zod";

const appRoot = path.resolve(fileURLToPath(new URL("..", import.meta.url)));
const contentRoot = path.join(appRoot, "content", "blog");
const outputPath = path.join(appRoot, "app", "lib", "markdown", "generated-posts.ts");

const frontMatterDate = z.preprocess((value) => {
  if (value instanceof Date) {
    return value.toISOString().slice(0, 10);
  }
  return value;
}, z.string().date());

const frontMatterSchema = z.object({
  author: z.string().min(1),
  cover: z.string().min(1),
  date: frontMatterDate,
  description: z.string().min(1),
  draft: z.boolean(),
  locale: z.enum(["zh-CN", "en"]),
  slug: z.string().min(1),
  tags: z.array(z.string().min(1)),
  title: z.string().min(1),
  updatedAt: frontMatterDate,
});

const codeFencePattern = /```([A-Za-z0-9_+-]*)[^\n]*\n([\s\S]*?)```/g;

async function main() {
  const files = await markdownFiles(contentRoot);
  const posts = [];

  for (const file of files) {
    const raw = await readFile(file, "utf8");
    const parsed = matter(raw);
    const meta = frontMatterSchema.parse(parsed.data);
    const relativePath = slash(path.relative(appRoot, file));
    const localeDir = relativePath.split("/").at(-2);

    if (localeDir !== meta.locale) {
      throw new Error(`${relativePath} locale mismatch: front matter is ${meta.locale}`);
    }

    const content = parsed.content.trim();
    posts.push({
      ...meta,
      content,
      highlightedCode: await highlightedCodeBlocks(content),
      path: relativePath,
    });
  }

  posts.sort((a, b) => `${a.locale}:${a.slug}`.localeCompare(`${b.locale}:${b.slug}`));
  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(
    outputPath,
    [
      'import type { BlogPost } from "./posts";',
      "",
      "// Generated by scripts/generate-blog-posts.mjs. Do not edit by hand.",
      `export const generatedBlogPosts = ${JSON.stringify(posts, null, 2)} satisfies BlogPost[];`,
      "",
    ].join("\n"),
    "utf8",
  );
}

async function markdownFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const entryPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        return markdownFiles(entryPath);
      }
      return entry.name.endsWith(".md") ? [entryPath] : [];
    }),
  );
  return files.flat().sort();
}

async function highlightedCodeBlocks(content) {
  const blocks = {};
  for (const match of content.matchAll(codeFencePattern)) {
    const language = match[1] || "text";
    const code = match[2].replace(/\n$/, "");
    const id = codeBlockId(language, code);
    try {
      blocks[id] = await codeToHtml(code, {
        lang: language,
        theme: "github-light",
      });
    } catch {
      blocks[id] = await codeToHtml(code, {
        lang: "text",
        theme: "github-light",
      });
    }
  }
  return blocks;
}

function codeBlockId(language, code) {
  let hash = 2166136261;
  for (const char of `${language}\0${code}`) {
    hash ^= char.charCodeAt(0);
    hash = Math.imul(hash, 16777619);
  }
  return (hash >>> 0).toString(36);
}

function slash(value) {
  return value.replace(/\\/g, "/");
}

await main();
